import pandas as pd

# Шаг 1: Создание DataFrame с оценками учеников по 5 предметам
data = {
    'Имя': ['Алиса', 'Боб', 'Чарли', 'Давид', 'Ева', 'Франк', 'Грета', 'Генри', 'Изабелла', 'Джон'],
    'Математика': [85, 78, 92, 88, 76, 95, 89, 84, 91, 77],
    'Физика': [79, 83, 88, 85, 80, 92, 81, 87, 90, 78],
    'Химия': [88, 79, 84, 90, 81, 85, 89, 92, 87, 80],
    'Биология': [92, 85, 88, 90, 86, 84, 91, 87, 85, 83],
    'История': [81, 89, 85, 87, 82, 88, 90, 84, 86, 85]
}

df = pd.DataFrame(data)

# Шаг 2: Вывод первых 5 строк DataFrame
print("Первые 5 строк DataFrame:")
print(df.head())

# Шаг 3: Вычисление средней оценки по каждому предмету
average_scores = df.mean(numeric_only=True)
print("\nСредняя оценка по каждому предмету:")
print(average_scores)

# Шаг 4: Вычисление медианной оценки по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианная оценка по каждому предмету:")
print(median_scores)

# Шаг 5: Вычисление Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
print(f"\nQ1 для оценок по математике: {Q1_math}")
print(f"Q3 для оценок по математике: {Q3_math}")

# Шаг 6: Вычисление межквартильного размаха (IQR) для оценок по математике
IQR_math = Q3_math - Q1_math
print(f"\nМежквартильный размах (IQR) для оценок по математике: {IQR_math}")

# Шаг 7: Вычисление стандартного отклонения по каждому предмету
std_dev_scores = df.std(numeric_only=True)
print("\nСтандартное отклонение по каждому предмету:")
print(std_dev_scores)
